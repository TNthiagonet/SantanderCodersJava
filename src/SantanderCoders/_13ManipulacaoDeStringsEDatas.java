package SantanderCoders;

public class _13ManipulacaoDeStringsEDatas {
    public static void main(String[] args) {

        String nome = "Thiago";
        // podemos usar o nome da variável + .manupulação
        // ex: nome.tipoDeManipulação


        // Utilizando toUpperCase (LETRAS MAIÚSCULAS)
        System.out.println(nome.toUpperCase());
        // Utilizando toLowerCase (letras minúsculas)
        System.out.println(nome.toLowerCase());
        // Utilizando length (Consultar qtd de letras)
        System.out.println(nome.length());

    }
}
