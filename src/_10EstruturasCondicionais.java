public class _10EstruturasCondicionais {
    public static void main(String[] args) {

        int nota = 70;

        // se nota maior ou igual 70, aprovado
        // if-else
        if (nota >= 70) {
            System.out.println("Aluno aprovado");
        } else {
            System.out.println("Aluno reprovado");
        }

    }
}



